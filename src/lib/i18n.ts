export function useLang() {
  const { values, set } = useResources()
  const lang = values[LANG_KEY] || 'en'
  const t = (en: string) => DICT[lang]?.[en] ?? en
  const setLang = (code: string) => set(LANG_KEY, code)
  const rtl = LANGUAGES.find((l) => l.code === lang)?.rtl ?? false
  return { lang, setLang, t, rtl }
}
